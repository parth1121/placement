import {API_HOST} from '../../../constants'

export const getApplicationData = (companyId, type, id, applicationId) => {
  var headers = new Headers();
  headers.append("Content-Type", "application/json");

  var requestOptions = {
    method: "GET",
    headers: headers,
    redirect: "follow",
  };
  return fetch(
    `${API_HOST}/application/${companyId}/${type}/${id}/${applicationId}`,
    requestOptions
  )
    .then((response) => {
      console.log(response);
      return response.json();
    })
    .catch((error) => console.log("error", error));
};
